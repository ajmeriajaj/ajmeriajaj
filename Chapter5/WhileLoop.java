public class WhileLoop {
    public static void main(String[] args) {
        /*
           Sometime we want our program to excecute few set of instruction over and over agian
           For example : I wanna print "Hello World" for 1000 times, so i don't print 1000 lines for my program, We used loops and same program done in few lines
           loops make it easy for us to tell the computer that a given set of instruction need to be execute repeatedly

           Basically 3 types of loops
           while
           do while
           for
         */

           int i = 1;
           while (i<=5) {
            System.out.println(i);
            i++;
           }
        
           
    }
}
