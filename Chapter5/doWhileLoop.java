public class doWhileLoop {
    public static void main(String[] args) {
        /*
           This loop is similar to a while loop except the fact that it is guaranteed to execute ay least one
         */
        // int a = 0;
        // while (a<5) {
        //     System.out.println(a);
        //     a++;
        // }

        int b = 0;
        do{
            System.out.println(b);
            b++;
        } while (b<5);
        

        int c = 1;
        do{
            System.out.println(c);
            c++;
        } while (c <= 45);
    }
}









